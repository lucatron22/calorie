public class Food {
    String food_name;
    double nf_calories;
    double nf_protein;
    double nf_total_carbohydrate;
    double nf_total_fat;

    public Food(String food_name, double nf_calories, double nf_protein, double nf_total_carbohydrate, double nf_total_fat) {
        this.food_name = food_name;
        this.nf_calories = nf_calories;
        this.nf_protein = nf_protein;
        this.nf_total_carbohydrate = nf_total_carbohydrate;
        this.nf_total_fat = nf_total_fat;
    }

    



}
